    private void jcrecepIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcrecepIdActionPerformed
            try{
            
            String recepId=jcrecepId.getSelectedItem().toString();
            ReceptionistPojo recep=ReceptionistDao.getReceptionistDetails(recepId);
            txtReceptionistName.setText(recep.getReceptionistName());
            jcGender.setSelectedItem(recep.getGender().toUpperCase());
            UserPojo user = UserDao.getRecepLogindetails(recep.getReceptionistName());
            txtLoginId.setText(user.getLoginId());
            txtPwd1.setText(user.getPassword());
            txtPwd2.setText(user.getPassword());
            EmpPojo recepUser =EmpDao.getEmployeeIdByName(recep.getReceptionistName());
            txtEmployeeId.setText(recepUser.getEmpId());
        }
                catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }//GEN-LAST:event_jcrecepIdActionPerformed

