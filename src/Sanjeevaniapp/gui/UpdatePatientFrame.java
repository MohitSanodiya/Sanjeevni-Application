    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs()){
            return;
        }
        try{
            AppointmentPojo appojo=new AppointmentPojo();
            appojo.setMobileNo(patient.getMno());
            appojo.setOpd(patient.getOpd());
            appojo.setPatientId(patient.getPatientId());
            appojo.setPatientName(patient.getFirstName()+" "+patient.getLastName());
            boolean result1=PatientDao.updatePatient(patient);
            boolean result2=AppointmentDao.updateAppointment(appojo);
             if(result1 && result2){
                JOptionPane.showMessageDialog(null,"Patient update successfully!!");
                ReceptionistOptionFrame option=new ReceptionistOptionFrame();
                this.dispose();
                option.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"Sorry! could not update the patient record!");
            } 
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in DB");
           ex.printStackTrace();
       }
    }//GEN-LAST:event_btnUpdateActionPerformed

