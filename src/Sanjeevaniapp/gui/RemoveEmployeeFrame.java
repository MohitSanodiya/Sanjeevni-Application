    private void btnRemoveEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveEmpActionPerformed
            String recepId=jcEmpId.getSelectedItem().toString();
        try{
            boolean result=EmpDao.deleteEmployeeById(recepId);
            if(result){
                JOptionPane.showMessageDialog(null, "Employee removed successfully!");
                jcEmpId.removeItem(recepId);
                if(jcEmpId.getItemCount()==0){
                    btnRemoveEmp.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "All Employees removed!");
           
                    ManageEmployeeFrame ManageEmployees = new ManageEmployeeFrame();
                    ManageEmployees.setVisible(true);
                    this.dispose();
                
            }
        }else{
              JOptionPane.showMessageDialog(null, "Deletion unsuccessful");  
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveEmpActionPerformed

