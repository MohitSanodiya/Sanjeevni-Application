 private void LoadReceptionistID() {
        try{
            List<String>employeeList=EmpDao.getAllemployeeId();
            for(String docId:employeeList){
                jcEmpId.addItem(docId);
            }
            if(jcEmpId.getItemCount()!=0)
                btnRemoveEmp.setEnabled(true);
            else
                btnRemoveEmp.setEnabled(false);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }
    }

