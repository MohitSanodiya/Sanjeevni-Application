    private void jcPatientIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcPatientIdActionPerformed
        {
           String patientId=jcPatientId.getSelectedItem().toString();
        if(patientId==null){
            JOptionPane.showMessageDialog(null, "Please select any patientId");
            return;
        }
        try{
           PatientPojo patList = new PatientPojo();
           patList =   PatientDao.findPatientDetailsById(patientId);
            if(patList ==null){
               JOptionPane.showMessageDialog(null, " Patient Record not found!");
               return;
            }
           txtOPD.setText(patList.getOpd());
           txtDoctorId.setText(patList.getDoctorId());
           txtAge.setText(String.valueOf(patList.getAge()));
           txtCity.setText(patList.getCity());
           txtAddress.setText(patList.getAddress());
           txtFirstName.setText(patList.getFirstName());
           txtLastName.setText(patList.getLastName());
           txtGender.setText(patList.getGender());
           txtDate.setText(String.valueOf(patList.getDate()));
           txtPhoneNo.setText(patList.getMno());
           jcM_Status.setSelectedItem(patList.getMarriedStatus());
       }
       catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Problem in DB:"+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
       }
     }
    }//GEN-LAST:event_jcPatientIdActionPerformed

