    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       
          
        if(!validateInputs()){
             JOptionPane.showMessageDialog(null,"Please Enter Valid Details !");
            return;
        }
        try{
             boolean result1 = ReceptionistDao.updateReceptionist(recep1);
             boolean result2 = UserDao.updateUser(user1);
            
        if(result1 && result2){
                JOptionPane.showMessageDialog(null,"Receptionist update successfully!!");
            }else{
                JOptionPane.showMessageDialog(null,"Sorry! could not update the receptionist record!");
            } 
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in DB");
           ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

